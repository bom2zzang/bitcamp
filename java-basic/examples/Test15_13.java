package bitcamp.java100;

public class Test15_13 {
 
    public static void main(String[] args) {
    
        int sum = 0;
        
        sum = sum + 100;
        sum = sum + 100;
        System.out.println(sum);
        
        sum = 0;
        sum += 100;
        sum += 100;
        System.out.println(sum);
        
        //복합대입연산자 [ > += -+ *= /= %= 
        
        
    }
    
}
