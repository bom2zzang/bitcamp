package bitcamp.java100;

public class Test11_4{        
    public static void main(String[] args){
    
        
        System.out.println(100);
        System.out.println(0144); //8진수의 100
        System.out.println(0x64); //16진수의 100, 0x로 시작한다.
        System.out.println(0X64);
        System.out.println(0b01100100); //2진수의 100, 0b로 시작한다.
        System.out.println(0B01100100);
        System.out.println(0B1100100); // 앞쪽의 0 생략가능
        
        
    }
}
